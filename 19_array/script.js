// Pole v TypeScriptu
var students = ['David', 'John', 'Lea'];
var numbers = [1, 4, 2, 8, 7];
var bool = [true, true, false, true, false];
students.push('Ester');
numbers.push(9);
bool.push(true);
console.log(students); //  ['David', 'John', 'Lea', 'Ester']
console.log(numbers); // [1, 4, 2, 8, 7, 9]
console.log(bool); // [true, true, false, true, false, true]
