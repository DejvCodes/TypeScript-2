/*
Co je to TypeScript
• TypeScript (zkráceně TS) je superset JavaScriptu – což znamená, že rozšiřuje JavaScript o 
  nové vlastnosti, ale zároveň je plně kompatibilní s běžným JavaScriptem

Jednoduše řečeno
• každý platný JS kód je zároveň platný TypeScriptový kód, ale ne naopak – TypeScript přidává 
  navíc hlavně statické typování a nástroje pro lepší kontrolu chyb už při psaní kódu

Funkce	       ->   JavaScript	                             TypeScript
Typování	     ->   • dynamické (typy se určují až za běhu)	 • statické (typy se kontrolují při kompilaci)
Kontrola chyb  ->   • až při spuštění	                       • už při psaní/kompilaci
Podpora OOP	   ->   • omezená	                               • silnější (třídy, rozhraní, generika, moduly)
Nástroje	     ->   • základní	                             • výborná integrace s VS Code, IntelliSense, refaktoring
Výstup	       ->   • spouští se přímo v prohlížeči	         • kompiluje se do čistého JavaScriptu
*/
 